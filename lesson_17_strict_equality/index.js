// strict equality
// = assignment operator
// == comparison operator (compare if value are equal)
// === strict equality operator (compare if datatype & value are equal)
// != inequality operator
// !== Strict inequality operator

const PI = 3.14;

if (PI === "3.14") {
  console.log("That is PI");
} else {
  console.log("that is not PI");
}

if (PI !== "3.14") {
  console.log("That is PI");
} else {
  console.log("that is not PI");
}
